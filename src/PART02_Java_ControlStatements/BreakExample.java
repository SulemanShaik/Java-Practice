package PART02_Java_ControlStatements;

public class BreakExample {
	public static void main(String[] args) {
		
		//Syntax
		/*jump-statement;    
		break; */
		
		//using for loop  
	    for(int i=1;i<=10;i++){  
	        if(i==5){  
	            //breaking the loop  
	            break;  
	        }  
	        System.out.println(i);  
	    }  
		
		
	}

}
