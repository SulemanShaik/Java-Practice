package PART02_Java_ControlStatements;

public class ContinueExample {
	public static void main(String[] args) {
		
		//Syntax
		/*jump-statement;    
		continue;   
		*/
		
		//for loop  
	    for(int i=1;i<=10;i++){  
	        if(i==5){  
	            //using continue statement  
	            continue;//it will skip the rest statement  
	        }  
	        System.out.println(i);  
	    }  
		
		}    
	}
	
	
	


