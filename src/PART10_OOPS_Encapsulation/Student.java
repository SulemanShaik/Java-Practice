package PART10_OOPS_Encapsulation;

public class Student {

	
		
		/*  Encapsulation - data binding/ wrapping data and methods in single unit
		 *  data hiding  - hiding the data outside the world so that they are not directly accessible
		 *  abstraction - force calling those variables and methods 
	    */
		 
		private String name;    //Private data members

		public String getName() {      //getter method for name
			return name;
		}

		public void setName(String name) {     //setter method for name
			this.name = name;
		}
		
		
		
	

}
